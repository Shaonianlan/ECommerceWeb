package mode;

public class Pet{
  private Long pet_id;
  private String pet_name;
  private String pet_description;
  private String pet_height;
  private String pet_type;
  private String pet_sourcearea;
  private String pet_stock;
  private String pet_image;
  private Double pet_price;
  private String pet_variety;

  public Long getPet_id() {
    return pet_id;
  }

  public void setPet_id(Long pet_id) {
    this.pet_id = pet_id;
  }

  public String getPet_name() {
    return pet_name;
  }

  public void setPet_name(String pet_name) {
    this.pet_name = pet_name;
  }

  public String getPet_description() {
    return pet_description;
  }

  public void setPet_description(String pet_description) {
    this.pet_description = pet_description;
  }

  public String getPet_height() {
    return pet_height;
  }

  public void setPet_height(String pet_height) {
    this.pet_height = pet_height;
  }

  public String getPet_type() {
    return pet_type;
  }

  public void setPet_type(String pet_type) {
    this.pet_type = pet_type;
  }

  public String getPet_sourcearea() {
    return pet_sourcearea;
  }

  public void setPet_sourcearea(String pet_sourcearea) {
    this.pet_sourcearea = pet_sourcearea;
  }

  public String getPet_stock() {
    return pet_stock;
  }

  public void setPet_stock(String pet_stock) {
    this.pet_stock = pet_stock;
  }

  public String getPet_image() {
    return pet_image;
  }

  public void setPet_image(String pet_image) {
    this.pet_image = pet_image;
  }

  public Double getPet_price() {
    return pet_price;
  }

  public void setPet_price(Double pet_price) {
    this.pet_price = pet_price;
  }

  public String getPet_variety() {
    return pet_variety;
  }

  public void setPet_variety(String pet_variety) {
    this.pet_variety = pet_variety;
  }
}
